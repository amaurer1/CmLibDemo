// IBlock
//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
class IBlock
{
// ELEMENT DATA
private:
	bool blocked;

// CONSTRUCTION / DESTRUCTION / ASSIGNMENT
public:
	IBlock() : blocked(false) {}

// ELEMENT FUNCTIONS
public:
	void setBlocked(const bool e) {blocked = e;}
	bool isBlocked() const {return blocked;}
};
